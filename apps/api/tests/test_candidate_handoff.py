async def _wait_job(client: httpx.AsyncClient, job_id: str) -> Dict[str, Any]:
    for _ in range(400):
        r = await client.get(f"/api/jobs/{job_id}")
        if r.json()["status"] in ("done", "failed", "cancelled"):
            return r.json()
        await asyncio.sleep(0.05)
    raise AssertionError(f"job {job_id} did not finish")


async def _run_explore(
    client: httpx.AsyncClient, project_id: str, n: int = 8, seed: int = 0
) -> Dict[str, Any]:
    """Run a tiny real exploration; returns the finished job dict."""
    resp = await client.post(
        f"/api/projects/{project_id}/explore",
        json={"n_samples": n, "seed": seed},
    )
    assert resp.status_code == 200, resp.text
    job = await _wait_job(client, resp.json()["job_id"])
    assert job["status"] == "done", job
    return job


async def _get_compressor(
    client: httpx.AsyncClient, project_id: str
) -> Dict[str, Any]:
    r = await client.get(f"/api/projects/{project_id}/components")
    assert r.status_code == 200, r.text
    comp = next(
        c for c in r.json()["components"] if c["kind"] == "Compressor"
    )
    return comp


def _expected_merge(params: Dict[str, Any]):
    """Run the normative merge directly — the test's first-principles oracle."""
    from routers._meanline_geom import build_cc_geometry

    return build_cc_geometry(sample=params)


