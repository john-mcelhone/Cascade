@pytest.mark.asyncio
async def test_jobs_list_scopes_by_project_and_shows_refusal_failed(app):
    transport = httpx.ASGITransport(app=app)
    async with httpx.AsyncClient(transport=transport, base_url="http://t") as client:
        # A refused cycle run on the blank aero seed (U1 refusal contract).
        r = await client.post(f"/api/projects/{AERO}/cycle/solve")
        assert r.status_code == 200, r.text
        refused = await _wait_job(client, r.json()["job_id"])
        assert refused["status"] == "failed"

        # A real explore on the microturbine.
        explore_job = await _run_explore(client, MICRO)

        aero_jobs = (
            await client.get("/api/jobs", params={"project_id": AERO})
        ).json()
        assert [j["kind"] for j in aero_jobs] == ["cycle"]
        assert aero_jobs[0]["status"] == "failed"
        # Refusal signature: error is None + structured failure envelope.
        assert aero_jobs[0]["error"] is None
        assert aero_jobs[0]["result"]["failure"]["kind"] == "design"

        micro_jobs = (
            await client.get("/api/jobs", params={"project_id": MICRO})
        ).json()
        assert [j["kind"] for j in micro_jobs] == ["explore"]
        # The explore job exposes best_id for the candidate detail link.
        assert (
            micro_jobs[0]["result"]["best_id"]
            == explore_job["result"]["best_id"]
        )

        # Unscoped list still returns everything (legacy behaviour),
        # newest first — the runs page reads top-down. The explore job was
        # created after the refused cycle run, so it must lead.
        all_jobs = (await client.get("/api/jobs")).json()
        assert len(all_jobs) == 2
        assert [j["id"] for j in all_jobs] == [
            micro_jobs[0]["id"],
            aero_jobs[0]["id"],
        ]
        created = [j["created_at"] for j in all_jobs]
        assert created == sorted(created, reverse=True)
