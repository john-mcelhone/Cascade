@pytest.mark.asyncio
async def test_handoff_writes_full_merged_geometry_as_plain_floats(app):
    """The bag holds build_cc_geometry's output, not bare sampled params."""
    transport = httpx.ASGITransport(app=app)
    async with httpx.AsyncClient(transport=transport, base_url="http://t") as client:
        job = await _run_explore(client, MICRO)
        best_id = job["result"]["best_id"]
        assert best_id is not None

        cand = (await client.get(f"/api/candidates/{best_id}")).json()
        # U8: the API model now exposes project_id (the in-memory dicts
        # always carried it).
        assert cand["project_id"] == MICRO

        resp = await client.post(
            f"/api/candidates/{best_id}/send-to-cycle",
            json={"project_id": MICRO},
        )
        assert resp.status_code == 200, resp.text

        comp = await _get_compressor(client, MICRO)
        gp = comp["params"]["geometry_params"]

        geom, op = _expected_merge(cand["params"])
        expected = {
            f.name: float(getattr(geom, f.name))
            for f in dataclasses.fields(geom)
            if getattr(geom, f.name) is not None
        }
        # Exact key set — the cycle builder's required fields plus the
        # explicit optionals the geometry object carries. The Sobol' key
        # name must NOT leak through (rename happened).
        assert set(gp.keys()) == set(expected.keys())
        assert "rotor_outlet_radius" not in gp
        for k, v in expected.items():
            assert isinstance(gp[k], (int, float)) and not isinstance(gp[k], bool)
            assert gp[k] == pytest.approx(v), k

        # The merged values are scaled/derived, not the raw sample: the
        # inducer dimensions must differ from the Eckardt reference (the
        # candidate r2 is far from the 0.200 m reference).
        assert gp["impeller_outlet_radius"] == pytest.approx(
            cand["params"]["rotor_outlet_radius"]
        )
        assert gp["inducer_tip_radius"] != pytest.approx(0.140)

        # RPM comes from the operating point (candidate-scaled, constant U2).
        assert comp["params"]["meanline_rpm_rpm"] == pytest.approx(op["rpm"])
        assert isinstance(comp["params"]["meanline_rpm_rpm"], float)

        # Provenance: the handoff records its source candidate as a plain
        # string — the UI chip reads exactly this key to attribute the
        # geometry.
        assert comp["params"]["geometry_source_candidate_id"] == best_id


