@pytest.mark.asyncio
async def test_manufacturability_candidate_id_overrides_fallback(app):
    transport = httpx.ASGITransport(app=app)
    async with httpx.AsyncClient(transport=transport, base_url="http://t") as client:
        await _run_explore(client, MICRO)
        cands: List[Dict[str, Any]] = (
            await client.get("/api/candidates")
        ).json()
        valid = [c for c in cands if c["status"] == "VALID"]
        assert len(valid) >= 2
        # Two candidates with distinct outlet radii.
        a, b = valid[0], valid[1]
        assert (
            a["params"]["rotor_outlet_radius"]
            != b["params"]["rotor_outlet_radius"]
        )

        # Pin A so the fallback heuristic resolves to A...
        r = await client.post(
            f"/api/candidates/{a['id']}/pin", json={"project_id": MICRO}
        )
        assert r.status_code == 200

        # ...then explicitly route B: the verdict must be B's geometry.
        resp = await client.get(
            f"/api/projects/{MICRO}/manufacturability",
            params={"candidate_id": b["id"]},
        )
        assert resp.status_code == 200, resp.text
        report = resp.json()
        assert report["candidate_id"] == b["id"]
        assert report["geometry"]["impeller_outlet_radius_m"] == pytest.approx(
            b["params"]["rotor_outlet_radius"]
        )

        # The un-routed call falls back to the pinned candidate A.
        fallback = (
            await client.get(f"/api/projects/{MICRO}/manufacturability")
        ).json()
        assert fallback["candidate_id"] == a["id"]


